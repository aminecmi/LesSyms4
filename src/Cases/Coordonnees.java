package Cases;

public class Coordonnees {
    private int vert;
    private int hor;

    public Coordonnees(int vert, int hor) {
        this.vert = vert;
        this.hor = hor;
    }

    public int getVert() {
        return vert;
    }

    public void setVert(int vert) {
        this.vert = vert;
    }

    public int getHor() {
        return hor;
    }

    public void setHor(int hor) {
        this.hor = hor;
    }
}
